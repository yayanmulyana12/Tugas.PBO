    private void loadOrderData() {
    // Kolom untuk tabel
    String[] columnNames = {"Nama Menu", "Jumlah", "Harga", "Tanggal", "Metode Pembayaran"};
    DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);

    // Ambil data terbaru dari OrderRepository
    OrderRepository repository = new OrderRepository();
    // Mengambil data pesanan terbaru dari repository yang berupa array
    List<Object[]> latestOrder = repository.getLatestOrderDetails();

    double totalHarga = 0; // Variabel untuk menghitung total harga

    // Tambahkan data terbaru ke model tabel dan hitung total harga
    for (Object[] order : latestOrder) {
        // Menambahkan setiap baris data pesanan ke dalam model tabel
        tableModel.addRow(order);
        totalHarga += (double) order[2]; // Harga ada di indeks ke-2
    }

    // Set model ke jTable1
    jTable1.setModel(tableModel);

    // Tampilkan total harga di text field
    totalHargaField.setText(String.valueOf(totalHarga));
}

