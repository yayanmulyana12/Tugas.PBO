    private void CetakTransaksiBT1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CetakTransaksiBT1ActionPerformed
         
       try {
        // Ambil nilai total harga dan uang yang dibayar
        double totalHarga = Double.parseDouble(totalHargaField.getText()); // Ambil total harga dari text field
        double uangDibayar = Double.parseDouble(bayar.getText()); // Ambil uang yang dibayar dari text field

        // Hitung kembalian
        double kembalian = uangDibayar - totalHarga;

        // Periksa apakah uang yang dibayar cukup
        if (kembalian < 0) {
            JOptionPane.showMessageDialog(this, "Uang yang dibayar tidak cukup!", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Hentikan proses jika uang tidak cukup
        } else {
            // Tampilkan hasil kembalian di text field Kembalian
            txtkembalian.setText(String.format("%.2f", kembalian)); // Format angka 2 decimal
        }
        
        // Membuat dokumen PDF
        Document document = new Document();
        
        // Tentukan path untuk menyimpan PDF di folder Dokumen pada C:
        String path = System.getProperty("user.home") + "\\Documents\\Transaksi.pdf"; 
        PdfWriter.getInstance(document, new FileOutputStream(path));

        // Buka dokumen
        document.open();

        // Tambahkan header ke PDF
        document.add(new Paragraph("Detail Transaksi"));
        document.add(new Paragraph("Tanggal: " + tanggal.getText())); // Tanggal transaksi
        document.add(new Paragraph("Nama: " + namaTextField.getText())); // Nama pengguna dari TextField
        document.add(new Paragraph(" ")); // Baris kosong

        // Membuat tabel PDF
        PdfPTable table = new PdfPTable(jTable1.getColumnCount());
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            table.addCell(jTable1.getColumnName(i)); // Menambahkan nama kolom ke tabel PDF
        }

        // Mengisi data dari JTable ke tabel PDF
        for (int row = 0; row < jTable1.getRowCount(); row++) {
            for (int col = 0; col < jTable1.getColumnCount(); col++) {
                table.addCell(jTable1.getValueAt(row, col).toString()); // Menambahkan data dari setiap cell JTable
            }
        }

        // Tambahkan tabel ke PDF
        document.add(table);

        // Tambahkan total harga dan kembalian
        document.add(new Paragraph(" "));
        document.add(new Paragraph("Total Harga: " + totalHargaField.getText()));
        document.add(new Paragraph("Uang Dibayar: " + bayar.getText()));
        document.add(new Paragraph("Kembalian: " + txtkembalian.getText()));

        // Tutup dokumen
        document.close();

        // Simpan data ke database
        PesananDao dao = new PesananDao();
        dao.simpanPesanan(namaTextField.getText(), Double.parseDouble(totalHargaField.getText()), "Cash", tanggal.getText());

        // Tampilkan pesan sukses
        JOptionPane.showMessageDialog(this, "PDF berhasil dicetak dan transaksi disimpan ke database!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal mencetak PDF!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_CetakTransaksiBT1ActionPerformed

